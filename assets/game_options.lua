module(..., package.seeall)
local music = 'on'
local sound = 'on'
local vibrate = 'off'


-----------------------------------------------
-- Set music on
-----------------------------------------------
function setMusicOn()
	music = 'on'
end

-----------------------------------------------
-- Set music off
-----------------------------------------------
function setMusicOff()
	music = 'off'
end

-----------------------------------------------
-- Get music status
-----------------------------------------------
function getMusic()
	return music
end

-----------------------------------------------
-- Set sound on
-----------------------------------------------
function setSoundOn()
	sound = 'on'
end

-----------------------------------------------
-- Set sound off
-----------------------------------------------
function setSoundOff()
	sound = 'off'
end

-----------------------------------------------
-- Get sound status
-----------------------------------------------
function getSound()
	return sound
end

-----------------------------------------------
-- Set vibration on
-----------------------------------------------
function setVibrateOn()
	vibrate = 'on'
end

-----------------------------------------------
-- Set vibration off
-----------------------------------------------
function setVibrateOff()
	vibrate = 'off'
end

-----------------------------------------------
-- Get vibration status
-----------------------------------------------
function getVibrate()
	return vibrate
end